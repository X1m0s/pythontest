# 1. 0 起始，-1 倒序
s = "abcdef"
s[0]==s[-6]  # 'a'
s[5]==s[-1]  # 'f'
# 2. 切片 [start:stop:step] 左闭右开，stop 不含
s[1:4]       # 'bcd'  不含索引4('e')
s[-4:-1]     # 'cde'  不含-1('f')
# 3. 步长为负 → start 必须 > stop
s[4:1:-1]    # 'edc'
s[1:4:-1]    # ''     走不通
# 4. 省略 = 取到尽头
s[2:]        # 'cdef'  start=2, 到末尾
s[:3]        # 'abc'   从开头, 到stop=3
s[::-1]      # 'fedcba' 全反转
# 5. 越界不报错（切片特性）
s[2:100]     # 'cdef'  自动截断
# s[100]     # IndexError  单索引越界报错
# 6. 不可变对象不支持索引赋值
# s[0]='x'  # TypeError  字符串元组不可改
lst=[1,2,3]; lst[0]=99   # ✓ 列表可变
# 7. del 可删索引/切片（可变序列）
del lst[0]; del lst[1:3]
# 8. 嵌套用多层索引
matrix=[[1,2],[3,4]]
matrix[0][1]  # 2