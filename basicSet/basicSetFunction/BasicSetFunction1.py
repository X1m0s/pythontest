#-----5.集合运算函数们(上)-----
#这里的函数均用于进行集合运算，返回值也为对应的运算结果，从差集计算开始
#set.difference(set)用于返回集合的差集，即返回的集合元素包含在第一个集合中，但不包含在第二个集合中
x={"apple","banana","cherry"}
y={"google","microsoft","apple"}
print(f"x.difference(y)={x.difference(y)}")
#difference_update(set)用于移除两个元素中均存在的元素，比起原版，修改为在原有集合中移除元素，无返回值
x={"apple","banana","cherry"}
y={"google","microsoft","apple"}
x.difference_update(y)
print(f"x={x}")
#set.intersection(set1,set...)用于返回两个或更多集合中都包含的元素，即交集
#参数为要查找相同元素的集合，返回值为一新集合，该集合的元素包含在所有集合中
x={"apple","banana","cherry"}
y={"google","runoob","apple"}
print(f"x.intersection(y)={x.intersection(y)}")
#多集合场景下
x={"a","b","c"}
y={"c","d","e"}
z={"f","g","c"}
print(f"x.intersection(y,z)={x.intersection(y,z)}")
#intersection.update()则用于移除集合中所有不存在于参数集合中的元素
x={"apple","banana","cherry"}
y={"google","runoob","apple"}
x.intersection_update(y)
print(f"x={x}")
#多集合场景下
x={"a","b","c"}
y={"c","d","e"}
z={"f","g","c"}
x.intersection(y,z)
print(f"x={x}")
#其实几个函数的update版本质来讲，就是将原来的函数从返回新集合修改为了原地修改原集合