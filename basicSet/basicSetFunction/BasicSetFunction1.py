#-----1.add()&update()-----
#均用于为集合添加元素，，添加类型不同，add()每次添加单个对象
fruits={"apple","banana","cherry"}
fruits.add("orange")
print(fruits)
fruits.add("apple")                 #已存在元素不做添加操作
print(fruits)
#update()可用于添加可迭代对象，但不是将可迭代对象本身加入，而是取出子元素逐一加入
fruits={"apple","banana","cherry"}
#fruits.update("orange")            #字符串属于可迭代对象，会被逐字母拆解后加入集合
#print(fruits)
fruits.update(["orange","grapes","coconut"])    #可使用列表或元组进行一层保护
print(fruits)
#因此也可以用来合并集合
set_a={1,2,3,4,5}
set_b={3,4,5,6,7}
set_a.update(set_b)
print(set_a)
#-----2.clear()-----
#该函数用于移除集合中的所有元素，但比起del会保留容器及所在内存
fruits={"apple","banana","cherry"} 
fruits.clear()
print(fruits) 
#-----3.copy()-----
#该函数用于浅拷贝集合，返回值为拷贝的集合
#(但因为集合内部即便嵌套其他数据结构，也只能嵌套可哈希的不能修改的结构，因此等于深拷贝)
sites={"Google","Runoob","Taobao"}
x=sites.copy()
print(x)
#-----4.remove()&discard()&pop()-----
#这三个函数均用于移除集合元素，但discard()比起remove()在移除不存在元素时不报错，而pop()则是"随机"移除
fruits={"apple","banana","cherry"}
fruits.remove("banana")
print(fruits)
fruits.discard("cherry")
print(fruits)
print(f"移除对象为:{fruits.pop()},移除后的集合为:{fruits}")     #因为删的只剩一个了，所以体现不出随机
