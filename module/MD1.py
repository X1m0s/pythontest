#如果没有给定参数，dir()函数会罗列出当前定义的所有名称

#python本身带有一些标准的模块库，后续会详细介绍，本处简单列出几个
#       模块名          功能描述
#       math            数学运算(如平方根、三角函数等)
#       os              操作系统相关功能(如文件、目录操作)
#       sys             系统相关的参数和函数
#       random          生成随机数
#       datetime        处理日期和时间
#       json            处理json数据
#       re              正则表达式操作
#       collections     提供额外的数据结构(如defaultdict、deque)
#       itertools       提供迭代器工具
#       functools       高阶函数工具(如reduce、lru_cache)
#有些模块直接被构建在解析器内，并会根据不同操作系统进行不同配置，sys会内置在每一个python解析中
