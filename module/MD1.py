#python支持将定义的方法和变量存放在文件中，为脚本或交互式解释器实例使用，该文件被称为模块
#python的模块(Module)是一个包含Python定义和语句的文件，文件名是模块名加.py后缀
#模块可以包含函数、类、变量以及可执行的代码，通过模块可以将代码组织成可重用的单元，便于管理及维护
#模块的作用包括以下几点
#1.代码复用:将常用的功能封装到模块中，可以在多个程序中重复使用
#2.命名空间管理:模块可以避免命名冲突，不同模块中的同名函数或变量不会互相干扰
#3.代码组织:将代码按功能划分到不同的模块中，使程序结构更清晰
#以下是一个使用python标准库中模块的例子
import sys
print("命令行参数如下:")
for i in sys.argv:
    print(i)
print("\n\nPython路径为:",sys.path,'\n')

#想使用Python源文件，只需要在另一个源文件里执行import语句，当解释器遇到import语句，如果模块在当前的搜索路径就会被导入
#如果想要导入模块，可以把模块文件和实际操作的文件放在同目录下
#模块的搜索顺序为:1.当前目录 2.环境变量PYTHONPATH指定的目录 3.Python标准库目录 4.pth文件中指定的目录

from fibo import fib,fib2           #这种导入方式不会把模块名称导入到字符表中
fib(10)
print(fib2(10))

#导入模块时，可以使用as关键字为模块或函数起别名，例如下文
