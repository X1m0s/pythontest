#模块除了方法定义，还可以包括可执行代码，这些代码一般用于初始化模块，并且只在第一次被导入时执行
#每个模块有独立的符号表，在模块内为所有的函数当作全局符号表来使用

#一个模块被另一个程序第一次引入时，其主程序将运行，如果需要在模块被引用时某一程序块不执行，可以用__name__属性来使该程序块仅在该模块自身运行时执行
#可以看文件夹下的using_name.py文件运行情况
import using_name
#如果模块是被直接运行，那么__name__的值为__main__；如果是被导入的，__name__的值为模块名

